    private void btncalcularpaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularpaActionPerformed
        // TODO add your handling code here:
        double alturapa,areapa,volumenpa,perimetropa,anchopa,longitudpa;
        if(txtaltura.getText().equals("")){
            JOptionPane.showConfirmDialog(null,"alturapa");
            txtalturapa.requestFocus();
        }else{
            if(txtlongitudpa.getText().equals("")){
                JOptionPane.showConfirmDialog(null,"longitudpa");
                txtlongitudpa.requestFocus();
            }else{
                if(txtanchopa.getText().equals("")){
                    JOptionPane.showConfirmDialog(null,"anchopa");
                    txtanchopa.requestFocus();
                }else{
                    alturapa=Double.parseDouble(txtalturapa.getText());
                    longitudpa=Double.parseDouble(txtlongitudpa.getText());
                    anchopa=Double.parseDouble(txtanchopa.getText());
                    
                    areapa= 2*(longitudpa*anchopa+longitudpa*alturapa+anchopa*alturapa);
                    volumenpa= longitudpa*anchopa*alturapa;
                    perimetropa=2*(longitudpa+anchopa);
                    txtareapa.setText(areapa+"");
                    txtvolumenpa.setText(volumenpa+"");
                    txtperimetropa.setText(perimetropa+"");
                    
                }
                
            }
        }
    }//GEN-LAST:event_btncalcularpaActionPerformed

