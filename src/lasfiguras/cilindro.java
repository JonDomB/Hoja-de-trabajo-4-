    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        // TODO add your handling code here:
        double altura,area,radio,radio2,volumen,perimetro;
        if(txtaltura.getText().equals("")){
            JOptionPane.showConfirmDialog(null,"Ingrese altura");
            txtaltura.requestFocus();
        }else{
            if(txtradio.getText().equals("")){
                 JOptionPane.showConfirmDialog(null,"Ingrese radio");
             txtradio.requestFocus();
            }else{
               altura=Double.parseDouble(txtaltura.getText());
                radio=Double.parseDouble(txtradio.getText());
                
                area=(2*Math.PI*radio*(altura+radio));
                radio2=Math.pow(radio,2);
                
                volumen=(Math.PI*radio2*altura);
                perimetro=(2*Math.PI*radio+2*altura);
                txtarea.setText(area+"");
                txtvolumen.setText(volumen+"");
                txtperimetro.setText(perimetro+"");
            }
        }
    }//GEN-LAST:event_btncalcularActionPerformed

