    private void btncalcularconoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularconoActionPerformed
        // TODO add your handling code here:
        double generatrizcono,areacono,radiocono,radio2,volumencono,perimetrocono,perimetrolateral;
        if(txtgeneratrizcono.getText().equals("")){
            JOptionPane.showConfirmDialog(null,"Ingrese la generatriz");
            txtgeneratrizcono.requestFocus();
        }else{
            if(txtradiocono.getText().equals("")){
                 JOptionPane.showConfirmDialog(null,"Ingrese radio");
             txtradiocono.requestFocus();
            }else{
               generatrizcono=Double.parseDouble(txtgeneratrizcono.getText());
                radiocono=Double.parseDouble(txtradiocono.getText());
                
                areacono=(Math.PI * radiocono * (radiocono + generatrizcono));
                radio2=Math.pow(radiocono,2);
                perimetrolateral = Math.PI * radiocono * generatrizcono;
                volumencono=(Math.PI*radio2*generatrizcono);
                perimetrocono=Math.PI * Math.pow(radiocono, 2);
                txtareacono.setText(areacono+"");
                txtvolumencono.setText(volumencono+"");
                txtperimetrocono.setText(perimetrocono+"");
                txtperimetrolateral.setText(perimetrolateral+"");
            }
        }
    }//GEN-LAST:event_btncalcularconoActionPerformed

